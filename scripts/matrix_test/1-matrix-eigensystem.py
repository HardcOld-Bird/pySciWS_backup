"""
测试2阶复矩阵的本征值和本征向量可视化。

该脚本使用 src/matrix.py 中的函数绘制一个常数复矩阵的本征系统。
"""

import sys
from pathlib import Path

import matplotlib.pyplot as plt
from sympy import I, symbols

# 添加项目根目录到sys.path
project_root = Path(__file__).parent.parent.parent
sys.path.insert(0, str(project_root))

from config.matplotlib_config import setup_chinese_fonts
from src.matrix import plot_2x2_matrix_eigensystem

# 配置中文字体
setup_chinese_fonts()

# %%
# ============================================================================
# 1. 定义符号和参数
# ============================================================================

# 定义一个虚拟参数（因为矩阵是常数，本征值/向量不会随参数变化）
x = symbols("x", real=True)

# %%
# ============================================================================
# 2. 定义常数复矩阵
# ============================================================================

# 矩阵元素（复常数）
m11 = -11.829873871050884 - 2.7518266505733493 * I
m12 = 0.4913758616356978 - 0.5733566219519516 * I
m21 = -25.799957780500414 + 1016.5317293119726 * I
m22 = 0.5985541118973402 + 31.838580801502193 * I

print("矩阵元素:")
print(f"  m11 = {m11}")
print(f"  m12 = {m12}")
print(f"  m21 = {m21}")
print(f"  m22 = {m22}")
print()

# %%
# ============================================================================
# 3. 绘制本征系统
# ============================================================================

# 绘制本征值和本征向量随参数x的变化
# 注意：由于矩阵元素为常数，本征值和本征向量不随x变化，图像显示为水平线
fig = plot_2x2_matrix_eigensystem(
    m11,
    m12,
    m21,
    m22,
    param_symbol=x,
    param_range=(0, 10),
    param_resolution=100,
    figsize=(14, 10),
)

# 添加总标题
fig.suptitle("常数复矩阵的本征系统", fontsize=16, y=0.995)

# 显示图像
plt.show()

print("图像已显示。")
print("注意：由于矩阵元素为常数，本征值和本征向量不随参数x变化。")
